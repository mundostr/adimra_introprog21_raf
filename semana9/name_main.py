def main():
	print("Inicia la ejecución de código")

# El uso de este control no es obligatorio al momento de escribir código Python,
# no obstante es una práctica habitual que verán también muchas veces cuando utilicen
# código generado por otros desarrolladores.
# Básicamente permite detectar si el script está siendo ejecutado de manera directa o no,
# y en base a ello indicar si se debe ejecutar alguna función de inicialización (como es
# el caso de main() en este ejemplo).
if (__name__ == "__main__"):
	main()
